print("Welcome to python!")
#"Welcome to python" is a string- a sequence of characters enclosed in double quotes
print("welcome","to","python!")
#the commas generate a space when the program is running
print("Welcome\nto\npython]")
#\n tells the output to move the cursor to the next line
print("Karan\tSoin")
#\t inserts a horizontal tab
print("Karan\\Soin")
#\\ inserts "\" in string
print("Karan\"Soin")
#\" inserts " " " in the string
print("Karan\'")
#\' inserts " ' " in the string
print("This is a very very \
      very long string")
#\ breaks string into pieces
print("Sum is",7+3)
#calculations can be done in the print statements